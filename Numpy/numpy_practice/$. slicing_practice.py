import numpy as np

A = np.array([[1, 2, 3, 4], [5, 6, 7, 8]], dtype=int)

# 1 2 3 4
# 5 6 7 8

# Q1.
# 2 3 4
# 6 7 8
print("Q1 : \n", A[:, 1:])

# Q2.
# 1    3
# 5    7
print("Q2 : \n", A[:, 0::2])

# Q3
# 2 3
print("Q3 : \n", A[0, 1:3])

